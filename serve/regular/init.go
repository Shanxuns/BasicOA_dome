package regular

var (
	Fname    = "^[A-Za-z0-9\u4e00-\u9fa5]{1,4}$"
	Lname    = "^[A-Za-z0-9\u4e00-\u9fa5]{1,2}$"
	Phone    = "^((13[0-9])|(14[5,7])|(15[0-3,5-9])|(17[0,3,5-8])|(18[0-9])|166|198|199|(147))\\d{8}$"
	Email    = "^[0-9a-z][_.0-9a-z-]{0,31}@([0-9a-z][0-9a-z-]{0,30}[0-9a-z].){1,4}[a-z]{2,4}$"
	Password = "^[a-zA-Z0-9_-]{6,64}$"
	Md5      = "^[a-zA-Z0-9]{32}$"
	Id       = "^[0-9]{1,11}$"
	True     = "^(0|1){1,1}$"
	Content  = "\\S{6,}"
	Avatar   = "data:image/((jpeg)|(png)|(jpg));base64,*"
	Title    = "^[A-Za-z0-9\u4e00-\u9fa5]{1,12}$"
	DateTime = "(\\d{2}|\\d{4})(?:\\-)?([0]{1}\\d{1}|[1]{1}[0-2]{1})(?:\\-)?([0-2]{1}\\d{1}|[3]{1}[0-1]{1})(?:\\s)?([0-1]{1}\\d{1}|[2]{1}[0-3]{1})(?::)?([0-5]{1}\\d{1})(?::)?([0-5]{1}\\d{1})"
	IsInt    = []string{Id, True}
)
